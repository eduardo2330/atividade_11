# Atividade 10 – Conversão de Unidades
# Peça ao usuário um valor em metros.
# Converta e mostre o resultado em centímetros e milímetros.
metros = float(input("coloque um valor em metros"))
centímetros = metros * 100
milímetros = metros * 1000
print(f"seu resultado é respectivamente em cm e mm {centímetros}, {milímetros}")